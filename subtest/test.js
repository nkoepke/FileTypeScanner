const fs = require('fs');

function getFileSizes(filePaths) {
  const fileSizes = {};

  // Durch jedes angegebene filePath iterieren
  for (const filePath of filePaths) {
    // Die Datei-Extension ermitteln
    const fileExtension = filePath.split('.').pop();

    // Wenn das Dateiformat noch nicht in fileSizes ist, fügen Sie es hinzu und setzen Sie die Größe auf 0
    if (!fileSizes[fileExtension]) {
      fileSizes[fileExtension] = 0;
    }

    // Die Größe der Datei ermitteln und zur entsprechenden Dateiformat-Eigenschaft hinzufügen
    const stats = fs.statSync(filePath);
    fileSizes[fileExtension] += stats.size;
  }

  // Ein Array von Objekten zurückgeben, das jedes Dateiformat und seine Größe enthält
  return Object.entries(fileSizes).map(([extension, size]) => ({
    extension,
    size
  }));
}

